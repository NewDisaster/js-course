const arr1 = ['A', 'B', []];
const arr2 = ['A', 'B', []];

const areArrayesEqual = (array1, array2) => {
  if (array1.length !== array2.length) {
    return false;
  }

  for (let i = 0; i < array1.length; i++) {
    const value1 = array1[i];
    const value2 = array2[i];

    const bothArrays = Array.isArray(value1) && Array.isArray(value2);

    if (bothArrays) {
      if (!areArrayesEqual(value1, value2)) {
        return false;
      }
    } else if (value1 !== value2) {
      return false;
    }
  }

  return true;
};

console.log(areArrayesEqual(arr1, arr2)); // true




